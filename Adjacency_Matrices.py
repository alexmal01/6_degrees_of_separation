def generate_matrix(n):
    result_matrix = [[0 for i in range(n)] for i in range(n)]
    for i in range(n):
        for j in range(n):
            result_matrix[i][j] = random.randint(0, 1)
    return result_matrix


def no_zeros(M):
    n = len(M)
    for i in range(n):
        for j in range(n):
            if M[i][j] == 0:
                return False
    return True


def degree_of_separation(M):
    power = 1
    result_matrix = matrix_power(M, 1)
    while not no_zeros(result_matrix):
        power += 1
        result_matrix = matrix_power(M, power)
    return power


